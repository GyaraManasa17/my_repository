#!/usr/bin/env python3

import rclpy
from rclpy.node import Node
import sys
import glob
import os

# Import the benchmark modules
from autocert_modules.robot_parser import RobotKinematicParser
from autocert_modules.workspace_sampler import WorkspaceSampler
from autocert_modules.workspace_analyzer import WorkspaceAnalyzer
from autocert_modules.workspace_coverage_analyzer import WorkspaceCoverageAnalyzer  # ✅ NEW: Imported Module 4

class BenchmarkRunner(Node):
    def __init__(self):
        super().__init__('benchmark_runner_node')
        self.get_logger().info("🚀 Initializing Robotic Arm Analysis Benchmark...")

        # Initialize module references
        self.parser = None
        self.sampler = None
        self.analyzer = None
        self.coverage_analyzer = None

    def run_benchmark(self):
        """
        Orchestrator method to run all benchmark modules sequentially.
        """
        try:
            # ==========================================
            # MODULE 1: Robot Kinematic Parser
            # ==========================================
            self.get_logger().info("--- [STARTING MODULE 1: URDF/SRDF Parsing] ---")
            
            self.parser = RobotKinematicParser(self)
            self.parser.load_and_parse(timeout_sec=10.0)
            
            self.get_logger().info("✅ Module 1 completed successfully.\n")

            # ==========================================
            # MODULE 2: Workspace Sampler
            # ==========================================
            self.get_logger().info("---[STARTING MODULE 2: Workspace Sampling] ---")
            
            self.sampler = WorkspaceSampler(self, self.parser)
            
            # ⚠️ Reminder: n_samples=100 is for quick testing. Change to 10000 later!
            self.get_logger().info("⚙️ Starting sampling process...")
            self.sampler.sample_workspace(n_samples=100, seed=42)
            
            self.get_logger().info("✅ Module 2 completed successfully.\n")

            # ==========================================
            # MODULE 3: Workspace Analyzer (Geometric)
            # ==========================================
            self.get_logger().info("--- [STARTING MODULE 3: Workspace Analysis (Geometric)] ---")
            
            # 🔍 Dynamically find the latest CSV file generated by Module 2
            csv_files = glob.glob("workspace_dataset_*.csv")
            if not csv_files:
                raise RuntimeError("❌ No workspace dataset CSV found! Did Module 2 fail to save?")
            
            # Get the most recently created CSV file
            latest_dataset = max(csv_files, key=os.path.getctime)
            self.get_logger().info(f"📁 Found dataset for analysis: {latest_dataset}")

            # Initialize and run the Analyzer
            self.analyzer = WorkspaceAnalyzer(self)
            metrics_geo = self.analyzer.analyze(latest_dataset)
            
            if metrics_geo:
                self.get_logger().info("✅ Module 3 completed successfully. Geometry plots saved!\n")
            else:
                self.get_logger().warning("⚠️ Module 3 finished, but no reachable points were found to analyze.\n")

            # ==========================================
            # MODULE 4: Workspace Coverage Analyzer
            # ==========================================
            self.get_logger().info("--- [STARTING MODULE 4: Workspace Coverage Analysis] ---")
            
            # Initialize and run the Coverage Analyzer
            # Notice we reuse 'latest_dataset' located in Module 3!
            self.coverage_analyzer = WorkspaceCoverageAnalyzer()
            metrics_cov = self.coverage_analyzer.analyze(latest_dataset)
            
            if metrics_cov:
                self.get_logger().info(f"📊 Coverage: {metrics_cov['workspace_coverage']:.4f}")
                self.get_logger().info(f"📊 Uniformity Score: {metrics_cov['uniformity_score']:.4f}")
                self.get_logger().info("✅ Module 4 completed successfully. Coverage plot saved!\n")

            # ==========================================
            # MODULE 5: [Placeholder for your next module]
            # ==========================================
            # Send it over whenever you are ready!

        except Exception as e:
            self.get_logger().error(f"❌ Benchmark aborted due to an error: {e}")
            sys.exit(1)


def main(args=None):
    rclpy.init(args=args)
    
    runner = BenchmarkRunner()
    
    try:
        runner.run_benchmark()
    except KeyboardInterrupt:
        runner.get_logger().info("🛑 Benchmark interrupted by user.")
    finally:
        # Clean shutdown
        runner.destroy_node()
        if rclpy.ok():
            rclpy.shutdown()

if __name__ == '__main__':
    main()




